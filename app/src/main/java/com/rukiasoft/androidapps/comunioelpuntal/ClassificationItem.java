package com.rukiasoft.androidapps.comunioelpuntal;


public class ClassificationItem {

    private String position;
    private String name;
    private String points;
    private GamerInformation gamerInformation;

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPoints() {
        return points;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public GamerInformation getGamerInformation() {
        return gamerInformation;
    }

    public void setGamerInformation(GamerInformation gamerInformation) {
        this.gamerInformation = gamerInformation;
    }


}
