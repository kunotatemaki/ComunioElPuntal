package com.rukiasoft.androidapps.comunioelpuntal;

public class ScoreItem {

    private String jornada = "";
    private String rankingRound = "";
    private String rankingGeneral = "";
    private String pointsRound = "";
    private String pointsGeneral = "";

    public String getRankingRound() {
        return rankingRound;
    }

    public void setRankingRound(String rankingRound) {
        this.rankingRound = rankingRound;
    }

    public String getRankingGeneral() {
        return rankingGeneral;
    }

    public void setRankingGeneral(String rankingGeneral) {
        this.rankingGeneral = rankingGeneral;
    }

    public String getPointsRound() {
        return pointsRound;
    }

    public void setPointsRound(String pointsRound) {
        this.pointsRound = pointsRound;
    }

    public String getPointsGeneral() {
        return pointsGeneral;
    }

    public void setPointsGeneral(String pointsGeneral) {
        this.pointsGeneral = pointsGeneral;
    }

    public String getJornada() {
        return jornada;
    }

    public void setJornada(String jornada) {
        this.jornada = jornada;
    }


}